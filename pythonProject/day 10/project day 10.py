def add(n1,n2):
    return n1 + n2

def subtract(n1,n2):
    return n1 - n2

def multiply(n1,n2):
    return n1 * n2

def divide(n1,n2):
    return n1  / n2

operations = {
    "+":add,
    "-":subtract,
    "*":multiply,
    "/":divide
}

def calculator():
    num1 = float(input("What is the first number?: "))
    for key in operations:
        print(key)
    Flag = True

    while Flag:
        operation_Symbol = input("Pick an Operation?: ")
        num2 = float(input("What is the next number?: "))
        calculation_function = operations[operation_Symbol]
        answer = calculation_function(num1,num2)

        print(f"{num1} {operation_Symbol} {num2} = {answer}")

        user = input("Do you wish to continue with previous number ? Type 'y' for yes and 'n' for no and Start a new calculation. ").lower()
        if user == "y":
            num1 = answer
        elif user == "n":
            Flag = False
            calculator()

calculator()



